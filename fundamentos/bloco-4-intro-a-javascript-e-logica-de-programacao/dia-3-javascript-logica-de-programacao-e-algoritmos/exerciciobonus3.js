let n = 5;


for (let index=1;index<=n;index+=1){
    if(index === 1){
        console.log("    *"); 
    }else if(index === 2){
        console.log("   **"); 
    }else if(index === 3){
        console.log("  ***"); 
    }else if(index === 4){
        console.log(" ****"); 
    }else if(index === 5){
        console.log("*****"); 
    }
}